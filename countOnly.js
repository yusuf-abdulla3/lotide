//TEST/ASSERTION FUNCTION
const assertEqual = require('./assertEqual');

//ACTUAL FUNCTION

const countOnly = (allItems, itemsToCount) => {
  const results = {};

  for (const item of allItems) {
    //console.log(item);
    
    if (itemsToCount[item]) { 
      if (results[item]) {
        results[item] += 1;
      } else {
        results[item] = 1;
      }
    }
  }

  return results;
}

module.exports = countOnly;

